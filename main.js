function oquvchiniTop(ism, group) {
    let isHas = false;
    for (let i = 0; i < group.length; i++) {
        if (group[i] === ism) {
            isHas = true;
        }
    }

    if (isHas === true) {
        return "Bor"
    } else {
        return "Yo'q"
    }
}

